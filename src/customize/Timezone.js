import TextField from '@material-ui/core/TextField';
import Autocomplete from '@material-ui/lab/Autocomplete';
import React from 'react';

export default function Timezone(props) {

    const { post, name, onReview, config } = props;

    const [options, setOptions] = React.useState([]);

    const [value, setValue] = React.useState({});

    React.useEffect(() => {

        let optionsInitial = Object.keys(country).map(key => {

            const group = Object.keys(country[key]).map(key2 => {

                if (key2 === post[name]) {

                    setValue({
                        title: country[key][key2],
                        value: key2,
                        filter: key
                    });
                }
                return {
                    title: country[key][key2],
                    value: key2,
                    filter: key
                }

            });

            return group;
        });

        let optionsInitial2 = [];

        optionsInitial.forEach((item) => {
            optionsInitial2 = optionsInitial2.concat(item);
        });

        setOptions(optionsInitial2);

    }, []);

    return (
        <Autocomplete
            fullWidth
            disableClearable
            options={options}
            onChange={(e, option) => { onReview(option.value); setValue(option); }}
            getOptionSelected={(option, value) => option.value === value.value}
            defaultValue={value}
            value={value}
            groupBy={(option) => option.filter}
            getOptionLabel={(option) => option.title ? option.title : ''}
            renderInput={(params) => <TextField {...params} label={config.title} fullWidth variant="outlined" />}
        />
    );
}

