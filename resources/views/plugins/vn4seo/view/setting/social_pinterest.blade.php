<h4>Pinterest settings</h4>
<p>Pinterest uses Open Graph metadata just like Facebook, so be sure to keep the Open Graph checkbox on the Facebook tab checked if you want to optimize your site for Pinterest.</p>
<p>If you have already confirmed your website with Pinterest, you can skip the step below.</p>
